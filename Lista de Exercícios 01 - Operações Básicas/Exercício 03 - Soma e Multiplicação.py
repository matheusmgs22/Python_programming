#Escreva um script que receba dois números inteiros e exiba a soma e a multiplicação dos números


number_one = int(input('Digite o primeiro número: '))
number_two = int(input('Digite o segundo número: '))

add_numbers = number_one + number_two
multiply_numbers = number_one * number_two

print(f'A soma de {number_one} + {number_two} é: {add_numbers}')
print(f'A multiplicação de {number_one} * {number_two} é: {multiply_numbers}')

